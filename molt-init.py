"""
molt-init -- one file, one run, a full Molt setup anywhere.

This script is fully self-contained: it carries its own copies of
molt-verify.py, molt-chain-append.py, molt-redact.py, the pre-commit hook,
and the CI workflow, embedded as base64. It does NOT need to be run from
inside a Molt checkout, and it does NOT need any other file to exist next to
it. Copy this one file anywhere, on any machine, with any setup (a brand
new Claude Code project, an existing repo, an empty folder) and run it.

What it does, in one run:
  - Writes out molt-verify.py, molt-chain-append.py, molt-redact.py,
    .githooks/pre-commit, and .github/workflows/molt-verify.yml, decoded
    from this file's own embedded copies -- the framework itself, identical
    across every adopter, not project-specific data.
  - Generates FRESH starter CLAUDE.md/AGENTS.md, memory/INDEX.md, and
    memory/decisions.md with one real example entry (the YYYY-MM-DD
    placeholder molt-verify.py already knows to warn about), not a copy of
    anyone else's decision history.
  - Ensures .gitignore protects CLAUDE.local.md/AGENTS.local.md, and
    .gitattributes sets merge=union for memory/decisions.md, without
    clobbering either file if it already exists.
  - Never overwrites a file that already exists, unless --force is passed.
    Reports created vs. skipped so nothing already there is silently lost.
  - Runs molt-verify.py against TARGET at the end and prints the verdict,
    plus the one remaining manual step (git config core.hooksPath).

Maintenance note (for this project's own repo, not for adopters): the
embedded blobs below are a snapshot, not a live link. If molt-verify.py,
molt-chain-append.py, molt-redact.py, the hook, or the CI workflow change,
run scripts/build-molt-init.py to regenerate them, then molt-verify.py's
check_init_embed_consistency will confirm they match again.

Usage:
    python3 molt-init.py TARGET_DIR [--force]

TARGET_DIR must already exist (an existing repo you're adopting Molt into,
or an empty directory for a fresh one). This script does not run `git init`
for you; that's your call to make, not this script's.
"""

